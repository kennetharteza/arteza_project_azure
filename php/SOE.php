								</ul><br>
								
								<p style="font-size:20px; font-weight:bold;">Program Outcome</p>
								<p>Elementary Education graduates are expected to able to:</p>
								<ul style="padding:15px; line-height:2em;">
									<li>Show an in-depth understanding of the diversity of learners in different learning areas</li>
									<li>Demonstrate meaningful and comprehensive pedagogical content knowledge of the different subject areas</li>
									<li>Utilize assessment and evaluation tools appropriately in measuring learning outcomes</li>
									<li>Make use of skills in communication, higher order thinking, and use of tools and technology to improve learning and teaching</li>
									<li>Exhibit positive attributes of a model teacher; both as an individual and a professional</li>
								</ul><br>
						
								<p style="font-size:20px; font-weight:bold;">Board Exam</p>
								<p>To become a Licensed teacher  in the Philippines, a graduate of a graduate of Bachelor of Elementary Education needs to pass the Licensure Examination
								for Teachers (LET). The examination is conducted by the Board of Professional Teachers under the supervision of the Professional Regulations Commission (PRC). 
								The LET exam is usually conducted twice a year during the month of January and July.</p><br>
								
								<p>There is also an option to take the Teacher Certificate Program (TCP), a non-degree program for graduates of any Bachelor’s degree who intend to become teachers. 
								TCP is considered to be the fastest way to get into the Teaching Profession, requiring only 18 units of Professional Education. Individuals who successfully completed 
								the TCP is given a certification and is qualified to take the Licensure Examination for Teachers (LET).</p><br>
								
