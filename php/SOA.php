								<p style="font-size:30px; font-weight:bold;">Bachelor of Science in Accountancy </p><br>
								<p style="font-size:20px; font-weight:bold;">Program Overview</p><br>
								
								<p>The Bachelor of Science in Accountancy (BSA) program is a five-year program focused on subjects in financial, public, and managerial accounting, 
								auditing, administration, business laws, and taxation. The program also teaches students to integrate information technology concepts into business
								systems, to create a more systematic and organized way of storing business-related data. Aside from business topics, BSA also equips students with
								a basic understanding of computer programming and auditing systems. <br><br>
								
								<p style="font-size:20px; font-weight:bold;">Recommended Senior High School Strand</p>
								<p>Students who want to pursue a degree in BS in Accountancy are encouraged to take the Accountancy, Business and Management strand. This strand
								provides the basic concepts of business and financial management, and corporate operations that will be helpful for college.  </p><br>
								
								<p style="font-size:20px; font-weight:bold;">Subjects and Curriculum</p>
								<ul style="padding:15px; line-height:2em;">
									<li>Law on Obligations and Contracts</li>
									<li>Introduction to the Philippine Government and Politics</li>
									<li>Business Laws and Regulations</li>
									<li>Regulatory Framework and Legal Issues in Business</li>
									<li>Management Science</li>
									<li>International Business and Trade</li>
									<li>Accounting Research Methods</li>
									<li>Statistical Analysis with Software Application</li>
									<li>Governance, Business Ethics, Risk Management, and Internal Control</li>
									<li>Managerial Economics</li>
									<li>Economic Development</li>
									<li>Financial Accounting and Reporting</li>
									<li>Conceptual Framework and Accounting Standard</li>
									<li>Intermediate Accounting 1, 2, and 3</li>
									<li>Accounting Information System</li>
									<li>Cost Accounting and Control</li>
								</ul><br>
								
								<p style="font-size:20px; font-weight:bold;">Program Outcome</p>
								<p>Graduates of BS in Accountancy are expected to be able to:</p>
								<ul style="padding:15px; line-height:2em;">
									<li>Handle business issues and problems in a global strategic perspective using the knowledge and technical skills in financial accounting, cost accounting, and management</li>
									<li>Conduct accountancy related research by properly using theories and methodologies of accounting</li>
									<li>Use technology as a business tool in generating financial and non-financial reports</li>
									<li>Maintain ethical practice and practice as an accountant</li>
								</ul><br>
								
								<p style="font-size:20px; font-weight:bold;">On-The-Job Training/Internship</p>
								<p>During the fourth year of the program, students are required to attend an On the Job Training (OJT) in various corporate and auditing companies.</p><br>
								<p>Students are given the opportunity to apply their knowledge and skills in an actual work setting. Usually, universities require them to submit a written report on their tasks, 
								learning experiences, and hours rendered. Their work performance will also be evaluated by their immediate managers and will be submitted to their respective OJT coordinator. 
								The number of hours required may differ in each university.</p><br>
								
								<p style="font-size:20px; font-weight:bold;">Career Opportunities</p>
								<p>To become a Certified Public Accountant in the Philippines, a graduate of BS in Accountancy needs to pass the Certified Public Accountant's Licensure Examination. 
								The examination is conducted by the Board of Accountancy under the supervision of the Professional Regulations Commission (PRC). </p><br>
								
								<p style="font-size:20px; font-weight:bold;">Career Opportunities</p>
								<p>Graduates of BS in Accountancy may pursue a career path in various corporate and auditing companies. They may for roles such as an accounting associate, audit staff,
								financial accounting and reporting staff, state accounting examiner, state accountant, and budget analyst.  </p>

