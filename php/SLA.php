								<p style="font-size:20px; font-weight:bold;">Program Overview</p><br>
								
								<p>The Bachelor of Science in Criminology or Criminal Justice is a four-year degree program intended for individuals who wish to have a career 
								in the fields of law enforcement, security administration, crime detection, and prevention of correctional administration. The program teaches 
								students the various theories, policies, practices, and laws associated with criminal behavior and the methods applied to manage deviant activities.</p><br>
								
								<p style="font-size:20px; font-weight:bold;">Subjects and Curriculum</p>
								<ul>
									<li>Introduction to Criminology</li>
									<li>Theories of Crime Causation</li>
									<li>Human Behavior and Victimology</li>
									<li>Professional Conduct and Ethical Standards</li>
									<li>Juvenile Delinquency and the Juvenile Justice System</li>
									<li>Dispute Resolution and Incidents Management</li>
									<li>Criminological Research</li>
								</ul><br>
								
								<p style="font-size:20px; font-weight:bold;">Program Outcome</p>
								<p>Graduates of BS in Criminology  are expected to be able to:</p>
								<ul >
									<li>Conduct criminological research on crimes, crime causation, victims, and offenders to include deviant behavior</li>
									<li>Internalize the concepts of human rights and victim welfare</li>
									<li>Demonstrate competence and broad understanding in law enforcement administration, public safety, and criminal justice</li>
									<li>Utilize criminalistics or forensic science in the investigation and detection of crime</li>
									<li>Abide with ethics in the practicing Psychology</li>
									<li>Apply the principles and jurisprudence of criminal law, evidence, and criminal procedure</li>
									<li>Ensure offenders’ welfare and development of their reintegration to the community</li>
								</ul><br>
								
								<p style="font-size:20px; font-weight:bold;">Board Exam</p><br>
								<p>To become a Licensed Criminologist in the Philippines, a graduate of BS in Criminology needs to pass 
								the Criminologist Licensure Examination. The examination is conducted by the Board of Criminology under
								the supervision of the Professional Regulations Commission (PRC). . </p>
								<br>
								
								<p style="font-size:20px; font-weight:bold;">Career Opportunities</p><br>
								<p>Graduates of BS in Criminology may pursue a career path in university settings, research 
								institutions, prison facilities, or law enforcement agencies. They may apply as a police patrol officer, 
								forensic photographer, court peace offer, discipline officer, or an investigator.</p>
