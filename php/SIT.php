							<p>Organization</p><br>
							<h2>Junior Philippine Computer Society</h2><br>
							<p>The Junior Philippine Computer Society (JPCS) is an organization of students in the field of information and communications technology. 
							The JPCS aims to provide its members with experiences that complement the knowledge members acquire in their respective schools, 
							thus better preparing each student for his future role as a world class ICT professional.</p><br>
		
							<p>JPCS membership benefits not only students but their schools as well. JPCS encourages friendly competition among schools.
							Competition winners not only win for themselves, but win for their school – in an inexpensive but very public opportunity to 
							advertise their school’s quality of education.</p><br>
		
							<p>As an organization that is actively involved in the ICT-related concerns of education, industry, business, and government, 
							the Philippine Computer Society is pleased to collaborate with schools to continuously improve the quality of ICT education and training.</p><br>
		
							<p>With a JPCS chapter in your school, graduating JPCS members can rightfully claim having been “bred”  in an organization that is an integral
							part of the Philippine Computer Society (PCS), the premier organization of  IT professionals in the country.  With a JPCS chapter 
							in your school, and with JPCS an integral part of PCS, your school is assured of an open direct channel between your school and the ICT industry.</p><br>
		
							<p>The Philippine Computer Society considers its linkages with the youth and the academe to be of extreme importance.  
							It aims to introduce JPCS to the individuals within and outside of JPCS – to the school’s officers, administrators, 
							deans, program heads, coordinators, advisers, faculty, and students.  A JPCS Steering Committee has been established to work closely
							with schools and their students in the organization and activation of a JPCS chapter in every school.  The Steering Committee may be 
							reached through the Philippine Computer Society office.
							</p><br><br><br><br>
		
