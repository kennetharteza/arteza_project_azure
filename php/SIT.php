								<p style="text-align:left; font-size:1.5em;">Program Offered</p><br><br>
								<p style="font-size:30px; font-weight:bold;">Bachelor of Science in Information Technology </p>
								
								<p>The Bachelor of Science in Information Technology (BS IT) is a four-year degree program that equips students with the basic ability to conceptualize, 
								design and implement software applications. It prepares students to be IT professionals who are able to perform installation, operation, development,
								maintenance, and administration of computer applications. The goal of the program is to produce information technologists who can assist individuals and organizations in s
								olving problems using information technology techniques and processes.</p><br><br>
								
								<p style="font-size:20px; font-weight:bold;">Recommended Senior High School Strand</p>
								<p>Students who wish to pursue a degree in BS in Information Technology are encouraged to take the Information and Communication Technology strand. 
								The strand equips students with basic topics on illustration, web design, animation, technical drafting, and computer programming.</p><br><br>
								
								<p style="font-size:20px; font-weight:bold;">Subjects and Curriculum</p>
								<ul style="padding:15px; line-height:2em;">
									<li>Introduction to Computing</li>
									<li>Fundamentals of Programming</li>
									<li>Intermediate Programming</li>
									<li>Data Structures and Algorithms</li>
									<li>Data Structures and Algorithms</li>
									<li>Information Management</li>
									<li>Applications Development and Emerging Technologies</li>
							
								</ul><br><br>
								
								<p style="font-size:20px; font-weight:bold;">Program Outcome</p>
								<p>Graduates of BS in Information Technology are expected to be able to:</p>
								<ul style="padding:15px; line-height:2em;">
									<li>Analyze complex problems and create an appropriate solution</li>
									<li>Design and develop computing solutions</li>
									<li>Utilize modern computing tools and skills</li>
									<li>Apply information technology concepts and practices</li>
									<li>Integrate information technology-based solutions</li>
									
								</ul><br><br>
								
								<p style="font-size:20px; font-weight:bold;">On-The-Job Training/Internship</p>
								<p>During the fourth year of the program, students are required to attend an On the Job Training (OJT) in IT firms and corporations, any government or private institutions.
								Students are given the opportunity to apply their knowledge and skills in an actual work setting. Usually, universities require them to submit a written report on their tasks, 
								learning experiences, and hours rendered. Their work performance will also be evaluated by their immediate managers and will be submitted to their respective OJT coordinator. 
								The number of hours required may differ in each university. </p><br><br>
								
								<p style="font-size:20px; font-weight:bold;">Board Exam</p>
								<p>The BS in Information Technology does not have a board examination. However, graduates may opt to take the Civil Service Examination (CSE) conducted by the Philippine 
								Civil Service Commission (PCSC) to qualify in working in government offices. Some graduates opt to take a Master’s degree followed by a Doctorate degree to gain expertise in the industry. </p><br><br>
								
								<p style="font-size:20px; font-weight:bold;">Career Opportunities</p>
								<p>Graduates of BS in Information Technology may pursue a career path in IT firms and corporations, any government or private institutions. They may apply as an applications developer,
								database administrator, technical support specialist, test engineer, web administrator, web developer, network administrator, information technology, information security administrator,
								network engineer, and systems analyst.</p>
