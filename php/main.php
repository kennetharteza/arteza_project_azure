				<h5>LATEST NEWS |</h5>
				<h2><strong>DWCC launches Official SMS; bridges school update, announcements</strong></h2>
				<h5>November 16, 5:41 pm</h5><br>
				<figure style="font-size: 11px;"><img src="../resources/smart2.png" width="100%"/>
						<figcaption><strong>REACH:</strong>DWCC thru Smart Comunication, to inform  students with the latest information.
						</figcaption>
					</figure><br><br>
				<p>Divine Word College of Calapan (DWCC) makes information dissemination easier through the launch
				of the DWCC Official SMS last November 11, as the institution subscribed to SMART Messaging Suite.</p><br>
				
				<p> All students, faculty employees, and administrators can now be informed offline through SMS 
				or text messages other than the DWCC Learning Management System (LMS) and social media platforms.</p><br>

				<p>The program was initiated by DWCC President Fr. Crispin Cordero under Marketing and Media 
				Affairs Office with the aid of the Information System Services Office.</p><br>
				
				<p>According to Officer-in-Charge of the Media Affairs Office, Ms. Claudette G. Sim, the main purpose 
				of the messaging suit is to reach students who are experiencing problems in internet connection thus allowing
				easier connectivity via SMS. </p><br>
				
				<p>Faculty members and students were advised to update their cellphone numbers and e-mail addressed in the
				DWCC Modular Academic Management System (MAMS) while employees were advised to acquaint their contact details 
				at the DWCC Human Resource Office which started on October 25 and ended four days later as per announced by
				Divine Word College of Calapan Online Community Facebook page.<p><br>
				
				<p><strong>“Mas okay ito, para sa akin. In case na makaligtaan na makita online, at least may message tayo na matatanggap,
				”</strong> Jaelou Andrea Belina, Bachelor of Secondary Education Major in English sophomore, 
				expressed after receiving the first message from DWCC Official SMS.</p><br>
				
				<p><strong>“Malaking bagay ‘yan para sa mga student ng school, during this time of pandemic, without leaving their home,
				they can message the school easily para sa mga questions and concerns nila. It’s like bridging the gap between the school 
				and the students,” stated May Ann P. Gawad, LPT, PhD, a faculty member of the DWCC SLA-SCJ.</strong></p><br>
			</article>
